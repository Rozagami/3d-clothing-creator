// Inicjalizacja sceny
const scene = new THREE.Scene();
const camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
const renderer = new THREE.WebGLRenderer({ antialias: true });
renderer.setSize(window.innerWidth, window.innerHeight);
renderer.shadowMap.enabled = true;
renderer.shadowMap.type = THREE.PCFSoftShadowMap;
document.body.appendChild(renderer.domElement);

// Dodanie ≈õwiat≈Ça (zmniejszona intensywno≈õƒá)
const ambientLight = new THREE.AmbientLight(0xffffff, 0.3);
scene.add(ambientLight);

const directionalLight = new THREE.DirectionalLight(0xffffff, 0.8);
directionalLight.position.set(3, 4, 5);
directionalLight.castShadow = true;
directionalLight.shadow.mapSize.width = 1024;
directionalLight.shadow.mapSize.height = 1024;
directionalLight.shadow.camera.near = 0.5;
directionalLight.shadow.camera.far = 50;
scene.add(directionalLight);

// Pod≈Ço≈ºe do cieni
const floorGeometry = new THREE.PlaneGeometry(30, 30);
const floorMaterial = new THREE.ShadowMaterial({ opacity: 0.3 });
const floor = new THREE.Mesh(floorGeometry, floorMaterial);
floor.rotation.x = -Math.PI / 2;
floor.position.y = -5; // Dostosowane do wiƒôkszego modelu
floor.receiveShadow = true;
scene.add(floor);

// Za≈Çadowanie modelu
const loader = new THREE.GLTFLoader();
let model;

loader.load('models/model.glb', function (gltf) {
    console.log("‚úÖ Model za≈Çadowany! üéâ");
    model = gltf.scene;

    // Ustawienie modelu
    model.traverse((node) => {
        if (node.isMesh) {
            node.castShadow = true;
            node.receiveShadow = true;
        }
    });
    model.scale.set(5, 5, 5); // **Powiƒôkszenie modelu 5x**
    model.position.set(0, -4, 0); // **Przesuniƒôcie modelu w d√≥≈Ç**

    scene.add(model);
    fitCameraToObject(camera, model);
    animate();
}, undefined, function (error) {
    console.error("‚ùå B≈ÇƒÖd ≈Çadowania modelu:", error);
});

// Automatyczne dopasowanie kamery do modelu
function fitCameraToObject(camera, object) {
    const boundingBox = new THREE.Box3().setFromObject(object);
    const center = boundingBox.getCenter(new THREE.Vector3());
    const size = boundingBox.getSize(new THREE.Vector3());

    const maxDim = Math.max(size.x, size.y, size.z);
    const fov = camera.fov * (Math.PI / 180);
    let cameraZ = Math.abs(maxDim / Math.sin(fov / 2));

    camera.position.set(center.x, center.y, cameraZ * 3); // **Kamera dalej od modelu**
    camera.lookAt(center);
}

// Obracanie modelem myszkƒÖ
let isDragging = false;
let previousMouseX = 0;

document.addEventListener("mousedown", (event) => {
    isDragging = true;
    previousMouseX = event.clientX;
});

document.addEventListener("mousemove", (event) => {
    if (isDragging && model) {
        let deltaX = event.clientX - previousMouseX;
        model.rotation.y += deltaX * 0.005; // Wolniejsze obracanie myszkƒÖ
        previousMouseX = event.clientX;
    }
});

document.addEventListener("mouseup", () => {
    isDragging = false;
});

// Animacja
function animate() {
    requestAnimationFrame(animate);
    if (model) {
        model.rotation.y += 0.001; // Wolniejszy automatyczny obr√≥t
    }
    renderer.render(scene, camera);
}

animate();
